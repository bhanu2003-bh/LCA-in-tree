int up[200007][20];
int level[200007];
void binarylifting(int node,int par,vector<int>adj[]){
    up[node][0] = par;
    for (int i = 1; i <20; i++)
    {
        if(up[node][i-1]!=-1){
             up[node][i] = up[up[node][i-1]][i-1];
        }
        else up[node][i] = -1;
    }
    for(auto it:adj[node]){
        if(it==par) continue;
        binarylifting(it,node,adj);
    }
    
}


void leveloftreenode(vector<int>adj[]){
 
queue<pair<int,pair<int,int>>>q;
level[1] = 1;
q.push({-1,{1,1}});
    while (!q.empty())
    {
        auto curr = q.front();
        q.pop();
     
         for(auto it:adj[curr.second.first]){
            if(it==curr.first) continue;
            level[it] = curr.second.second+1;
            q.push({curr.second.first,{it,level[it]}});
         }
    }
    
}

int lift(int node,int jump){
    if(jump==0 || node==-1) return node;

    for (int i = 19; i>=0; i--)
    {
       if(jump&(1<<i)) return lift(up[node][i],jump-(1<<i));
    }
     
}
int lca(int node1,int node2){
    if(level[node1]<level[node2]) swap(node1,node2);

    node1 = lift(node1,level[node1]-level[node2]);

    int high = level[node2];
    int low = 0;


    while(low!=high){
        int mid = (high+low)/2;
   
        int x = lift(node1,mid);
        int y = lift(node2,mid);
    // cout<<x<<" "<<y<<" "<<mid<<"\n";
        if(x==y){
            high = mid;
        }
        else low = mid+1;
      
        
    }
    return lift(node1,high);
}

